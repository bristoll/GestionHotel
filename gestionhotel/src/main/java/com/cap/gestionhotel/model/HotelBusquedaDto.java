package com.cap.gestionhotel.model;

public class HotelBusquedaDto {

	private int ha_id;
	private float ha_precio;
	private int ha_personas;
	private String ho_nombre;
	private String ho_direccion;
	private int ho_id;
	public int getHa_id() {
		return ha_id;
	}
	public void setHa_id(int ha_id) {
		this.ha_id = ha_id;
	}
	public float getHa_precio() {
		return ha_precio;
	}
	public void setHa_precio(float ha_precio) {
		this.ha_precio = ha_precio;
	}
	public int getHa_personas() {
		return ha_personas;
	}
	public void setHa_personas(int ha_personas) {
		this.ha_personas = ha_personas;
	}
	public String getHo_nombre() {
		return ho_nombre;
	}
	public void setHo_nombre(String ho_nombre) {
		this.ho_nombre = ho_nombre;
	}
	public String getHo_direccion() {
		return ho_direccion;
	}
	public void setHo_direccion(String ho_direccion) {
		this.ho_direccion = ho_direccion;
	}
	public int getHo_id() {
		return ho_id;
	}
	public void setHo_id(int ho_id) {
		this.ho_id = ho_id;
	}
	
	
	
}
